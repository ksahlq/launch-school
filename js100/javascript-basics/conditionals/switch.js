let animal = 'horse';

switch (animal) {
  case 'duck':
    console.log('quack');
    break;
  case 'squirrel':
    console.log('nook nook');
    break;
  case 'horse':
    console.log('neigh');
    break;
  case 'bird':
    console.log('tweet tweet');
    break;
  default:
    console.log('*cricket*');
}